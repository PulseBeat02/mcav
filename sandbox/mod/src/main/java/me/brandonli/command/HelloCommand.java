package me.brandonli.command;

import com.mojang.brigadier.CommandDispatcher;
import com.mojang.brigadier.context.CommandContext;
import net.minecraft.server.command.CommandManager;
import net.minecraft.server.command.ServerCommandSource;
import net.minecraft.text.Text;

public final class HelloCommand {

  public static void register(final CommandDispatcher<ServerCommandSource> dispatcher) {
    dispatcher.register(CommandManager.literal("hello").executes(HelloCommand::execute));
  }

  private static int execute(final CommandContext<ServerCommandSource> context) {
    final ServerCommandSource source = context.getSource();
    source.sendFeedback(() -> Text.literal("Hello!"), false);
    return 1;
  }

  private HelloCommand() {}
}
